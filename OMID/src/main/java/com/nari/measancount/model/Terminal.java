package com.nari.measancount.model;

import java.util.List;

public class Terminal {
	private String terminalId;
	private String terminalAdd;
	private String terminalState;
	private String tmnlColor;
	private String tmnlStateMent;
	private List<Collector> comms;
	public String getTerminalId() {
		return terminalId;
	}
	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}
	public String getTerminalAdd() {
		return terminalAdd;
	}
	public void setTerminalAdd(String terminalAdd) {
		this.terminalAdd = terminalAdd;
	}
	public String getTerminalState() {
		return terminalState;
	}
	public void setTerminalState(String terminalState) {
		this.terminalState = terminalState;
	}
	public String getTmnlColor() {
		return tmnlColor;
	}
	public void setTmnlColor(String tmnlColor) {
		this.tmnlColor = tmnlColor;
	}
	public String getTmnlStateMent() {
		return tmnlStateMent;
	}
	public void setTmnlStateMent(String tmnlStateMent) {
		this.tmnlStateMent = tmnlStateMent;
	}
	public List<Collector> getComms() {
		return comms;
	}
	public void setComms(List<Collector> comms) {
		this.comms = comms;
	}
	
}
