package com.nari.vcm.dataMining.po;


import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/************客户群对象PO*************/

@Entity
@Table(name="S_CUST_GROUP_OBJ")
public class S_Cust_Group_Obj {
	
	private int cust_Group_Obj_ID;
	private int Cust_ID;
	private int Cust_Group_ID;
	
	@Id
	public int getCust_Group_Obj_ID() {
		return cust_Group_Obj_ID;
	}
	public void setCust_Group_Obj_ID(int custGroupObjID) {
		cust_Group_Obj_ID = custGroupObjID;
	}
	public int getCust_ID() {
		return Cust_ID;
	}
	public void setCust_ID(int custID) {
		Cust_ID = custID;
	}
	public int getCust_Group_ID() {
		return Cust_Group_ID;
	}
	public void setCust_Group_ID(int custGroupID) {
		Cust_Group_ID = custGroupID;
	}
	
	
	
	
	
}
