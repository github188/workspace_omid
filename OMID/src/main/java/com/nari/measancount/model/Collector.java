package com.nari.measancount.model;

import java.util.List;

public class Collector {
	private String collectorId;
	private String collectAdd;
	private String collectState;
	private String color;
	private String stateMent;
	private List<Meter> meters;
	public String getCollectorId() {
		return collectorId;
	}
	public void setCollectorId(String collectorId) {
		this.collectorId = collectorId;
	}
	public String getCollectAdd() {
		return collectAdd;
	}
	public void setCollectAdd(String collectAdd) {
		this.collectAdd = collectAdd;
	}
	public String getCollectState() {
		return collectState;
	}
	public void setCollectState(String collectState) {
		this.collectState = collectState;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getStateMent() {
		return stateMent;
	}
	public void setStateMent(String stateMent) {
		this.stateMent = stateMent;
	}
	public List<Meter> getMeters() {
		return meters;
	}
	public void setMeters(List<Meter> meters) {
		this.meters = meters;
	}
	
}
