package com.nari.measancount.model;

import java.util.List;

public class Line {
	private String lineId;
	private String lineName;
	private List<Substation> subs;
	public String getLineId() {
		return lineId;
	}
	public void setLineId(String lineId) {
		this.lineId = lineId;
	}
	public String getLineName() {
		return lineName;
	}
	public void setLineName(String lineName) {
		this.lineName = lineName;
	}
	public List<Substation> getSubs() {
		return subs;
	}
	public void setSubs(List<Substation> subs) {
		this.subs = subs;
	}
	

}
