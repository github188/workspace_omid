package com.nari.measancount.model;

public class Meter {
	private String meterId;
	private String meterAddr;
	private String MeterState;
	private String stateColor;
	private String eventLevelName;
	private String eventName;
	private String assetNo;
	private String consName;
	private String consNo;
	public String getMeterId() {
		return meterId;
	}
	public void setMeterId(String meterId) {
		this.meterId = meterId;
	}
	public String getMeterAddr() {
		return meterAddr;
	}
	public void setMeterAddr(String meterAddr) {
		this.meterAddr = meterAddr;
	}
	public String getMeterState() {
		return MeterState;
	}
	public void setMeterState(String meterState) {
		MeterState = meterState;
	}
	public String getStateColor() {
		return stateColor;
	}
	public void setStateColor(String stateColor) {
		this.stateColor = stateColor;
	}
	public String getEventLevelName() {
		return eventLevelName;
	}
	public void setEventLevelName(String eventLevelName) {
		this.eventLevelName = eventLevelName;
	}
	public String getEventName() {
		return eventName;
	}
	public void setEventName(String eventName) {
		this.eventName = eventName;
	}
	public String getAssetNo() {
		return assetNo;
	}
	public void setAssetNo(String assetNo) {
		this.assetNo = assetNo;
	}
	public String getConsName() {
		return consName;
	}
	public void setConsName(String consName) {
		this.consName = consName;
	}
	public String getConsNo() {
		return consNo;
	}
	public void setConsNo(String consNo) {
		this.consNo = consNo;
	}
	
}
